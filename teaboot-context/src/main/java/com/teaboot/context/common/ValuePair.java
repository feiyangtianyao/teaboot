package com.teaboot.context.common;

public class ValuePair {
	Object value;
	Object pair;
	
	public Object getValue() {
		return value;
	}
	public void setValue(Object value) {
		this.value = value;
	}
	public Object getPair() {
		return pair;
	}
	public void setPair(Object pair) {
		this.pair = pair;
	}
}
